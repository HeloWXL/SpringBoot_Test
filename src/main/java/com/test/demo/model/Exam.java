package com.test.demo.model;

public class Exam {
    private Integer examId;

    private Integer examSelect;

    private Integer examBlank;

    private Integer teacherId;

    private Integer courseId;

    public Integer getExamId() {
        return examId;
    }

    public void setExamId(Integer examId) {
        this.examId = examId;
    }

    public Integer getExamSelect() {
        return examSelect;
    }

    public void setExamSelect(Integer examSelect) {
        this.examSelect = examSelect;
    }

    public Integer getExamBlank() {
        return examBlank;
    }

    public void setExamBlank(Integer examBlank) {
        this.examBlank = examBlank;
    }

    public Integer getTeacherId() {
        return teacherId;
    }

    public void setTeacherId(Integer teacherId) {
        this.teacherId = teacherId;
    }

    public Integer getCourseId() {
        return courseId;
    }

    public void setCourseId(Integer courseId) {
        this.courseId = courseId;
    }
}